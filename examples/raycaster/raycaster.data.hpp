void load_assets(spritesheet &ss) {
	uint8_t bit_depth = 6;

	rgba palette[64];

	for (auto i = 0; i < pow(2, bit_depth); i++) {
		int r = packed_data[20 + (i * 4) + 0];
		int g = packed_data[20 + (i * 4) + 1];
		int b = packed_data[20 + (i * 4) + 2];
		int a = packed_data[20 + (i * 4) + 3];
		palette[i] = rgba(r, g, b, a);
	}

	const uint8_t *pixel_data = &packed_data[20 + (32 * 4)];

	uint8_t image[160 * 160];
	uint8_t *p = image;

	uint32_t src_byte_count = (sizeof(image) / 8) * bit_depth;

	uint8_t col = 0;
	uint8_t bit = 0;

	for (auto i = 0; i < src_byte_count; i++) {
		uint8_t b = *pixel_data++;
		for (auto j = 0; j < 8; j++) {
			col <<= 1;
			col |= ((0b10000000 >> j) & b) ? 1 : 0;

			bit++;
			if (bit == bit_depth) {
				*p = col;

				p++;
				bit = 0;
				col = 0;
			}
		}
	}

	ss.load(palette, image);
 }